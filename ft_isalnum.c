#include "libft.h"

int	ft_isalnum(int c)
{
	int				flag;

	flag = 0;
	if (c >= 'a' && c <= 'z')
		flag = 1;
	else if (c >= 'A' && c <= 'Z')
		flag = 1;
	else if (c >= '0' && c <= '9')
		flag = 1;
	return (flag);
}
