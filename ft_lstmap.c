#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*first;
	t_list	*new;

	new = ft_lstnew(f(lst->content));
	if (!new)
		return (NULL);
	first = new;
	lst = lst->next;
	while (lst)
	{
		new->next = ft_lstnew(f(lst->content));
		if (!new->next)
		{
			ft_lstclear(&first, del);
			break ;
		}
		new = new->next;
		lst = lst->next;
	}
	return (first);
}
